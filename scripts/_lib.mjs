/** octo-docs repo root. */
export const DOCS_ROOT = path.resolve(__dirname, '..');
/** Parent dir holding the sibling repos (octo-cli, octo-auth, octo-server, …). */
export const WORKSPACE = path.resolve(DOCS_ROOT, '..');

